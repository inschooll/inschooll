/**
 * This component allows users to select between dark mode and light mode 
 * and it has a Continue button that allows users proceed to the next component
 * @returns 
 */
export default function SelectTheme() {
  const {displayNewComponent: displayNextComponent} = useContext(OnboardingContext);
  const nextComponent = <AreYouASchoolCreator />;
  const [isDark, setIsDark] = useState<boolean>();

  useEffect(() => {
    const theme = getTheme();

    // update current theme
    if (theme == "dark") {
      setIsDark(true);
      setTheme(constants.theme.dark);
    }
    else {
      setIsDark(false)
      setTheme(constants.theme.light);
    }
    
  }, []);

  const updateSelection = (isDark: boolean) => {
    setIsDark(isDark);
    setTheme(isDark ? constants.theme.dark : constants.theme.light);
  };

  return (
    <>
      <div className="space-y-8 text-center ">
        <OnboardingTitleAndDescription
          title="Pick your style"
          description="You can always change this later in the application."
        />

        <div className="rounded-lg md:flex">
          <ChooseContainer
            className="rounded-t-sm sm:rounded-t-none md:rounded-l-lg"
            isSelected={!isDark}
            onClick={updateSelection.bind(null, false)}
          >
            <Light className="scale-[.8]" />
          </ChooseContainer>
          <ChooseContainer
            className="rounded-b-sm sm:rounded-b-none md:rounded-r-lg"
            isSelected={isDark}
            onClick={updateSelection.bind(null, true)}
          >
            <Dark className="scale-[.8]" />
          </ChooseContainer>
        </div>
      </div>

      <OnboardingButton
        onClick={displayNextComponent?.bind(null, nextComponent)}
      />
    </>
  );
}

